import UIKit

extension UIColor {
    
    open class var yellowCustom: UIColor {
        get {
            return UIColor(red:0.976, green:0.620, blue:0.004, alpha: 1.000)
        }
    }
    
    open class var pinkCustom: UIColor {
        get {
            return UIColor(red:0.859, green:0.188, blue:0.412, alpha: 1.000)
        }
    }
    
}
